		public void modificarSocio(Socio socio){

				jTextNombre.setText(socio.getNombre());
				jTextApellidos.setText(socio.getApellidos());
				jTextDNI.setText(socio.getDNI() );
				jTextEmail.setText(socio.getEmail());
				jFormattedTextFechaNacimiento.setText(sdf.format(socio.getFechaDeNacimiento()));
                if(socio.getSexo()=='M')
                    radioSexoFemeninoSocio.setSelected(true);
                else
                    radioSexoMasculinoSocio.setSelected(true);
                jTextLocalidad.setText(socio.getLocalidad());
                jTextProvincia.setText(socio.getProvincia());
                jTextDomicilio.setText(socio.getDireccion());
                jTextCP.setText(socio.getCP());  
                jTextTelefono.setText(socio.getTelefonoFijo());
                jTextMovil.setText(socio.getTelefonoMovil());
                jTextUsuario.setText(socio.getUsuario());
				labelError.setVisible(false);
		}
		
		public void escribirSocioDatos(String[] datos){

				jTextNombre.setText(datos[0]);
				jTextApellidos.setText(datos[1]);
				jTextDNI.setText(datos[2]);
				jTextEmail.setText(datos[3]);
				jFormattedTextFechaNacimiento.setText(datos[4]);
                if(datos[5].equals("M"))
                    radioSexoFemeninoSocio.setSelected(true);
                else
                    radioSexoMasculinoSocio.setSelected(true);
                jTextLocalidad.setText(datos[6]);
                jTextProvincia.setText(datos[7]);
                jTextDomicilio.setText(datos[8]);
                jTextCP.setText(datos[9]);  
                jTextTelefono.setText(datos[10]);
                jTextMovil.setText(datos[11]);
                jTextUsuario.setText(datos[12]);
				labelError.setVisible(false);
				
				jTextCantidadColaboracion.setText(datos[13]);
				jFormattedTextFechaColaboracion.setText(datos[14]);
				jTextConcepto.setText(datos[15]);
				if(!"".equals(datos[16]))
					cbTipoColaboracion.setSelectedIndex(Integer.parseInt(datos[18]));
				else
					cbTipoColaboracion.setSelectedIndex(0);
				jFormattedTextFechaInicioColaboracion.setText(datos[17]);
				jFormattedTextFechaFinColaboracion.setText(datos[18]);
				jTextCantidadCuota.setText(datos[19]);
				jTextIntevaloPago.setText(datos[20]);
				jFormattedTextFechaIniCuota.setText(datos[21]);
				jFormattedTextFechaFinCuota.setText(datos[22]);
		}
